

function abrirmenu()
{
   ele=document.getElementById("menu");

    if((ele.style.display!="block") )
    {
       ele.style.display="block";
    }
    else 
    {
       ele.style.display="none";
    }
    
}


function closemenu()
{
   ele=document.getElementById("menu");

   if((ele.style.display=="block") )
   {
      ele.style.display="none";
   }
}









